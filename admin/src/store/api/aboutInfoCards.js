import http from "../../services/http";
import { handleErrors } from "../../helpers/error";
import {
  setAboutInfoCards,
  loadingAboutInfoCards,
  deleteAboutInfoCards,
} from "../../store/cms/aboutInfoCards";

const path = "/cms/about_info_cards";

export const loadAboutInfoCards = ({
  body = {},
  onSuccess,
  onError,
  onEnd,
} = {}) => {
  return async (dispatch, getState) => {
    dispatch(loadingAboutInfoCards(true));
    try {
      //showing loader on ui
      const res = await http.get({ url: path });
      const { data } = res.data;

      dispatch(setAboutInfoCards(data));

      //onSuccess event firing
      if (onSuccess) onSuccess(res);
    } catch (err) {
      //onError event firing
      console.log("Error => ", err);
      if (onError) onError(err);
      else handleErrors(err);
    } finally {
      if (onEnd) onEnd();
      dispatch(loadingAboutInfoCards(false));
    }
  };
};

