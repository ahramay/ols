import http from "../../services/http";
import { handleErrors } from "../../helpers/error";
import {
  setStudents,
  loadingStudents,
  deleteStudent,
} from "../../store/cms/students";

const path = "/cms/student_reviews_slider";

export const loadStudents = ({ body = {}, onSuccess, onError, onEnd } = {}) => {
  return async (dispatch, getState) => {
    dispatch(loadingStudents(true));
    try {
      //showing loader on ui
      const res = await http.get({ url: path });
      const { data } = res.data;

      dispatch(setStudents(data));

      //onSuccess event firing
      if (onSuccess) onSuccess(res);
    } catch (err) {
      //onError event firing
      console.log("Error => ", err);
      if (onError) onError(err);
      else handleErrors(err);
    } finally {
      if (onEnd) onEnd();
      dispatch(loadingStudents(false));
    }
  };
};

